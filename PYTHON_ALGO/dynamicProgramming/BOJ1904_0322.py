# N=4일 때는 0011, 0000, 1001, 1100, 1111 등 총 5개
# 15746으로 나눈 나머지를 출력한다.

# n = 1 : 1
# n = 2 : 00 11
# n = 3 : f(1)+00 / f(2)+1 >> 100 001 111
# n = 4 : 0000 0011 1001 1100 1111
#             > f(2)+00 / f(3)+1
# n = 5 : 00001 00100 10000 00111 10011 11001 11100 11111
#             > 00+f(3) / 1+f(4)

# 피보나치 형식으로 만들수 있음

import sys
input = sys.stdin.readline

n = int(input())
ans = [0]*1000001
ans[1], ans[2] = 1,2
for i in range(3,n+1):
    ans[i] = (ans[i-1] + ans[i-2])%15746

print(ans[n])