# 스트링(문자열)​을 입력한 후, 스트링(문자열)​을 구성하는 단어들 중, 

# 현재 단어 목록에 포함이 되어있지 않은 단어를 단어 목록의 가장 뒤에 추가하는 프로그램을 작성하시오.

# (1) 처음에는 단어의 목록이 하나도 없다.

# (2) 단어의 구분은 공백으로 한다.

# (3) 스트링은 계속 입력받으며, 프로그램이 종료되지 않는 이상 기존의 단어들의 목록은 계속 유지된다.

# (4) 목록에 단어가 없을 경우 단어를 목록의 가장 뒤에 추가하고, 있을경우 추가하지 않는다.

# (5) 단어 목록에는 입력되는 순서대로 저장된다.

# (6) 알파벳 대.소문자는 구분된다(다르다).

# 입력되는 스트링(문자열)의 최대 길이는 100이하이며, 스트링(문자열)​은 최대 10개 이하이다.

# 하나의 결과가 나온 후에도 계속 새로운 입력을 받다가, 스트링(문자열)​ 값이 "END"이면 프로그램을 종료한다.
# 현재의 단어 목록을 공백으로 구분하여 한 줄에 한 번씩 출력한다.

def add_words_to_list():
    word_list = []  # 초기 단어 목록
    while True:
        string = input("문자열을 입력하세요 (종료하려면 'END' 입력): ")
        if string == "END":  # 입력이 'END'이면 반복 종료
            break
        for word in string.split():  # 입력된 문자열을 단어로 분리
            if word not in word_list:  # 단어가 목록에 없으면 추가
                word_list.append(word)
        print(" ".join(word_list))  # 현재 단어 목록 출력

add_words_to_list()